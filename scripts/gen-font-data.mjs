#!/usr/bin/env node
/**
 * Generate the tree-shakeable font data tables under
 * src/lib/pdf-engine/core/fonts/data/ from authoritative upstream sources:
 *
 *   - glyph-list.ts       Adobe Glyph List (glyphname -> Unicode).
 *                         Source: adobe-type-tools/agl-aglfn/glyphlist.txt
 *   - encodings.ts        Standard / WinAnsi / MacRoman / PDFDoc / Symbol /
 *                         ZapfDingbats base encodings (code -> glyphname).
 *                         Source: mozilla/pdf.js src/core/encodings.js
 *   - standard-metrics.ts base-14 AFM widths + vertical metrics per glyph name.
 *                         Source: @pdf-lib/standard-fonts (bundled AFM data)
 *
 * These files are committed so the engine never touches the network or fs at
 * runtime (critical for the browser worker). Re-run with network access when the
 * upstream tables change:  node scripts/gen-font-data.mjs
 */
import { writeFileSync, mkdirSync, readFileSync, existsSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";
import { createRequire } from "node:module";

const require = createRequire(import.meta.url);
const __dirname = dirname(fileURLToPath(import.meta.url));
const OUT_DIR = resolve(__dirname, "..", "src", "lib", "pdf-engine", "core", "fonts", "data");

const AGL_URL = "https://raw.githubusercontent.com/adobe-type-tools/agl-aglfn/master/glyphlist.txt";
const ENC_URL = "https://raw.githubusercontent.com/mozilla/pdf.js/master/src/core/encodings.js";
const AGL_CACHE = "/tmp/glyphlist.txt";
const ENC_CACHE = "/tmp/encodings.js";

const HEADER =
  "/**\n * AUTO-GENERATED by scripts/gen-font-data.mjs - DO NOT EDIT BY HAND.\n" +
  " * Regenerate with: node scripts/gen-font-data.mjs\n */\n\n";

async function fetchText(url, cache) {
  if (existsSync(cache)) return readFileSync(cache, "utf-8");
  const res = await fetch(url);
  if (!res.ok) throw new Error(`fetch ${url}: ${res.status}`);
  const text = await res.text();
  try {
    writeFileSync(cache, text);
  } catch {
    /* cache best-effort */
  }
  return text;
}

/** Escape a JS string as \uXXXX code-unit escapes (ASCII-safe TS source). */
function esc(str) {
  let out = "";
  for (let i = 0; i < str.length; i++) {
    out += "\\u" + str.charCodeAt(i).toString(16).padStart(4, "0").toUpperCase();
  }
  return out;
}

// --------------------------------------------------------------------------
// 1. Adobe Glyph List
// --------------------------------------------------------------------------
function buildGlyphList(aglText) {
  const entries = [];
  for (const line of aglText.split("\n")) {
    if (!line || line.startsWith("#")) continue;
    const semi = line.indexOf(";");
    if (semi < 0) continue;
    const name = line.slice(0, semi);
    const codes = line
      .slice(semi + 1)
      .trim()
      .split(/\s+/)
      .map((h) => parseInt(h, 16));
    if (codes.some((c) => Number.isNaN(c))) continue;
    const str = codes.map((c) => String.fromCodePoint(c)).join("");
    entries.push([name, str]);
  }
  entries.sort((a, b) => (a[0] < b[0] ? -1 : a[0] > b[0] ? 1 : 0));
  let body = HEADER;
  body += "/** Adobe Glyph List: glyph name -> Unicode string. */\n";
  body += "export const AGL: Record<string, string> = {\n";
  for (const [name, str] of entries) {
    body += `  ${JSON.stringify(name)}: "${esc(str)}",\n`;
  }
  body += "};\n";
  return { body, count: entries.length };
}

// --------------------------------------------------------------------------
// 2. Base encodings
// --------------------------------------------------------------------------
async function buildEncodings(encText) {
  // The pdf.js module is a self-contained ESM; import it directly.
  const mod = await import("file://" + ENC_CACHE);
  const pad = (arr) => {
    const a = arr.slice(0, 256);
    while (a.length < 256) a.push("");
    return a.map((s) => s || "");
  };
  const standard = pad(mod.StandardEncoding);
  const winAnsi = pad(mod.WinAnsiEncoding);
  const macRoman = pad(mod.MacRomanEncoding);
  const symbol = pad(mod.SymbolSetEncoding);
  const zapf = pad(mod.ZapfDingbatsEncoding);

  // PDFDocEncoding (ISO 32000-1 Annex D.2). Only used for document text strings,
  // but included for completeness. ASCII + Latin-1 high range mirror WinAnsi;
  // 0x18-0x1F and 0x80-0xA0 carry the PDFDoc-specific set.
  const pdfDoc = new Array(256).fill("");
  for (let c = 0x20; c <= 0x7e; c++) pdfDoc[c] = winAnsi[c];
  for (let c = 0xa1; c <= 0xff; c++) pdfDoc[c] = winAnsi[c];
  const pdfDocSpecial = {
    0x18: "breve", 0x19: "caron", 0x1a: "circumflex", 0x1b: "dotaccent",
    0x1c: "hungarumlaut", 0x1d: "ogonek", 0x1e: "ring", 0x1f: "tilde",
    0x80: "bullet", 0x81: "dagger", 0x82: "daggerdbl", 0x83: "ellipsis",
    0x84: "emdash", 0x85: "endash", 0x86: "florin", 0x87: "fraction",
    0x88: "guilsinglleft", 0x89: "guilsinglright", 0x8a: "minus",
    0x8b: "perthousand", 0x8c: "quotedblbase", 0x8d: "quotedblleft",
    0x8e: "quotedblright", 0x8f: "quoteleft", 0x90: "quoteright",
    0x91: "quotesinglbase", 0x92: "trademark", 0x93: "fi", 0x94: "fl",
    0x95: "Lslash", 0x96: "OE", 0x97: "Scaron", 0x98: "Ydieresis",
    0x99: "Zcaron", 0x9a: "dotlessi", 0x9b: "lslash", 0x9c: "oe",
    0x9d: "scaron", 0x9e: "zcaron", 0xa0: "Euro",
  };
  for (const [k, v] of Object.entries(pdfDocSpecial)) pdfDoc[Number(k)] = v;

  const emit = (name, arr) => {
    let s = `export const ${name}: readonly string[] = [\n`;
    for (let i = 0; i < 256; i += 8) {
      s += "  " + arr.slice(i, i + 8).map((g) => JSON.stringify(g)).join(", ") + ",\n";
    }
    s += "];\n\n";
    return s;
  };

  let body = HEADER;
  body += "/** Base text encodings: character code (0-255) -> glyph name (\"\" = undefined). */\n\n";
  body += emit("STANDARD_ENCODING", standard);
  body += emit("WIN_ANSI_ENCODING", winAnsi);
  body += emit("MAC_ROMAN_ENCODING", macRoman);
  body += emit("PDF_DOC_ENCODING", pdfDoc);
  body += emit("SYMBOL_ENCODING", symbol);
  body += emit("ZAPF_DINGBATS_ENCODING", zapf);
  body +=
    'export type BaseEncodingName =\n' +
    '  | "StandardEncoding"\n' +
    '  | "WinAnsiEncoding"\n' +
    '  | "MacRomanEncoding"\n' +
    '  | "PDFDocEncoding"\n' +
    '  | "Symbol"\n' +
    '  | "ZapfDingbats";\n\n';
  body +=
    "export const BASE_ENCODINGS: Record<BaseEncodingName, readonly string[]> = {\n" +
    "  StandardEncoding: STANDARD_ENCODING,\n" +
    "  WinAnsiEncoding: WIN_ANSI_ENCODING,\n" +
    "  MacRomanEncoding: MAC_ROMAN_ENCODING,\n" +
    "  PDFDocEncoding: PDF_DOC_ENCODING,\n" +
    "  Symbol: SYMBOL_ENCODING,\n" +
    "  ZapfDingbats: ZAPF_DINGBATS_ENCODING,\n" +
    "};\n";
  return { body };
}

// --------------------------------------------------------------------------
// 3. base-14 AFM metrics
// --------------------------------------------------------------------------
function buildStandardMetrics() {
  const { Font, FontNames } = require("@pdf-lib/standard-fonts");
  const names = Object.values(FontNames);
  let widthsBody = "";
  let metricsBody = "";
  for (const fontName of names) {
    const f = Font.load(fontName);
    const widths = {};
    for (const m of f.CharMetrics) widths[m.N] = m.WX;
    const pairs = Object.entries(widths).sort((a, b) => (a[0] < b[0] ? -1 : 1));
    widthsBody += `  ${JSON.stringify(fontName)}: {\n`;
    for (const [n, w] of pairs) widthsBody += `    ${JSON.stringify(n)}: ${w},\n`;
    widthsBody += "  },\n";

    const bbox = f.FontBBox.map(Number);
    const ascent = f.Ascender || bbox[3];
    const descent = f.Descender || bbox[1];
    metricsBody += `  ${JSON.stringify(fontName)}: { ascent: ${ascent}, descent: ${descent}, bbox: [${bbox.join(", ")}] },\n`;
  }
  let body = HEADER;
  body += "/** base-14 AFM glyph widths (1000 units per em), keyed by glyph name. */\n";
  body += "export const BASE14_WIDTHS: Record<string, Record<string, number>> = {\n";
  body += widthsBody;
  body += "};\n\n";
  body += "export interface FontMetrics {\n  ascent: number;\n  descent: number;\n  bbox: [number, number, number, number];\n}\n\n";
  body += "/** base-14 vertical metrics (1000 units per em). */\n";
  body += "export const BASE14_METRICS: Record<string, FontMetrics> = {\n";
  body += metricsBody;
  body += "};\n";
  return { body, count: names.length };
}

async function main() {
  mkdirSync(OUT_DIR, { recursive: true });
  const aglText = await fetchText(AGL_URL, AGL_CACHE);
  await fetchText(ENC_URL, ENC_CACHE);

  const agl = buildGlyphList(aglText);
  writeFileSync(resolve(OUT_DIR, "glyph-list.ts"), agl.body);
  console.log(`glyph-list.ts   (${agl.count} glyph names)`);

  const enc = await buildEncodings();
  writeFileSync(resolve(OUT_DIR, "encodings.ts"), enc.body);
  console.log(`encodings.ts    (6 base encodings)`);

  const met = buildStandardMetrics();
  writeFileSync(resolve(OUT_DIR, "standard-metrics.ts"), met.body);
  console.log(`standard-metrics.ts (${met.count} fonts)`);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
